package Modelo;

public class ServiciosExtra {

    private String idHospedaje;
    private String Descripcion;
    private long idServiciosExtra;

    public String getIdHospedaje() {
        return idHospedaje;
    }

    public void setIdHospedaje(String idHospedaje) {
        this.idHospedaje = idHospedaje;
    }

    public String getDescripcion() {
        return Descripcion;
    }

    public void setDescripcion(String descripcion) {
        Descripcion = descripcion;
    }

    public long getIdServiciosExtra() {
        return idServiciosExtra;
    }

    public void setIdServiciosExtra(long idServiciosExtra) {
        this.idServiciosExtra = idServiciosExtra;
    }

}
